#include "../libft.h"

char	*ft_strdup(const char *s1)
{
	char *s2;

	s2 = ft_strnew(ft_strlen(s1));
	if (s2 == NULL)
		return (NULL);
	ft_strcpy(s2, s1);
	return (s2);
}
